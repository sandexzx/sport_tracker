function renderRegions(paths, keyToMuscle, highlights, onClick) {
  return Object.entries(paths).map(([key, d]) => {
    const muscle = keyToMuscle[key];
    const h = highlights[muscle];
    return (
      <path
        key={key}
        d={d}
        className="muscle-region"
        fill={h?.color || '#e5e5e5'}
        fillOpacity={h?.opacity ?? 0.3}
        stroke="rgba(44,36,32,0.15)"
        strokeWidth="0.5"
        onClick={() => onClick?.(muscle)}
        role="button"
        tabIndex={0}
        aria-label={muscle}
        onKeyDown={(e) => {
          if (e.key === 'Enter' || e.key === ' ') onClick?.(muscle);
        }}
      />
    );
  });
}

/**
 * SVG body outline with highlightable muscle regions.
 * @param {'front'|'back'} view
 * @param {{ [muscleName]: { color: string, opacity: number } }} highlights
 * @param {(muscleName: string) => void} onClick
 */
export default function MuscleBody({ view = 'front', highlights = {}, onClick }) {
  const isFront = view === 'front';
  const paths = isFront ? FRONT_PATHS : BACK_PATHS;
  const keyToMuscle = isFront ? FRONT_KEY_TO_MUSCLE : BACK_KEY_TO_MUSCLE;
  const outline = isFront ? FRONT_OUTLINE : BACK_OUTLINE;
  const label = isFront ? 'Спереди' : 'Сзади';

  return (
    <div className="muscle-body">
      <svg viewBox="0 0 200 410" className="muscle-body__svg" aria-label={label}>
        {/* body outline */}
        <path d={outline} fill="#f5f0eb" stroke="#c4b8ad" strokeWidth="1" />
        {/* muscle regions */}
        {renderRegions(paths, keyToMuscle, highlights, onClick)}
      </svg>
      <span className="muscle-body__label">{label}</span>
    </div>
  );
}
